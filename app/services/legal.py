def _org(ss: dict) -> dict:
    return {
        "name": ss.get("site_name") or "Фабрика-кухня Вкусно",
        "legal": ss.get("legal_info") or "",
        "email": ss.get("contact_email") or "",
        "phone": ss.get("contact_phone") or "",
        "address": ss.get("contact_address") or "",
        "site": ss.get("site_url") or "vkusnotomsk.ru",
    }


